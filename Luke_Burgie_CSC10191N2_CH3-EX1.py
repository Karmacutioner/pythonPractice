#Day of the week
print('Input a number 1 through 7')
n = input()
if n == '1':
      print('Monday')
elif n == '2':
      print('Tuesday')
elif n == '3':
      print('Wednesday')
elif n == '4':
      print('Thurdsay')
elif n == '5':
      print('Friday')
elif n == '6':
      print('Saturday')
elif n == '7':
      print('Sunday')
else:
      print('invalid number')
